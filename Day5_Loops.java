public class Day5_Loops{
    public static void main(String[] args){
        String c="Harrrrr";
        System.out.println(c);
        System.out.println(c.length());
        System.out.println(c.substring(0, 5));
        System.out.println(c.charAt(4));
        System.out.println(c.length()-1);
        System.out.println("hello".equals("hello"));
        // To Get Last 3 characters
        System.out.println(c.substring(c.length()-3));
        String result=1+"b";
        System.out.println(result);
        System.out.println("hello"+2+2);
        System.out.println("hello"+(2+2));
        


    }
}